public class ExemploForArray {
    public static void main(String[] args) {
        String alunos [] = {"Matheus" , "Fabio", "Juliana", "Abençoado"};

        for (int x = 0 ; x < alunos.length; x++){
            System.out.println("Aluno no indice x=" + x + " é o : " + alunos [x]);

        }
    }
    
}
